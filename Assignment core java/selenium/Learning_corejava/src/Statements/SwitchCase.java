package Statements;

public class SwitchCase {
public static void main(String[] args) {
	
	int choice = 1;
	switch(choice)
	{
	case 1:
		System.out.println("Gujarati");
		break;
	case 2:
		System.out.println("English");
	case 3:
		System.out.println("Hindi");
	default :
		System.out.println("Invalid");
}
}
}