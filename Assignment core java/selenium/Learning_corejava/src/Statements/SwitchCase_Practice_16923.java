package Statements;

public class SwitchCase_Practice_16923 {
	
	public static void main(String[] args) {
		
		int Choice = 1;        // change choice and get the answer
		switch(Choice) {
			case 1 :
			System.out.println("English");
			break;
			case 2 :
			System.out.println("Gujarati");
			break;
			case 3 :
			System.out.println("Hindi");
			break;
			default :
			System.out.println("Invalid");
			
		}
		
	}
}