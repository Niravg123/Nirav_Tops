package oparators;

public class Arithmetic_Practice_15923 {
public static void main(String[] args) {
	
	int a=10;
	int b=20;
	System.out.println(a+b); // answer 30
	System.out.println(a-b); // answer -10
	System.out.println(a*b); // answer 200
	System.out.println(a/b); // answer 0
	System.out.println(a+"tops"); // answer 10tops
	System.out.println("tops"+a); // answer tops10
	System.out.println(a+b+"tops"); // answer 30tops
	System.out.println("tops"+a+b); // answer tops1020
	
}			
}
 